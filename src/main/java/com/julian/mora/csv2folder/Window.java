    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser fileChooser = new JFileChooser();

        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        fileChooser.setDialogTitle("Seleccionar archivo CSV");
        fileChooser.setMultiSelectionEnabled(false);

        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            try {
                File selectedFile = fileChooser.getSelectedFile();
                System.out.println("Selected file: " + selectedFile.getAbsolutePath());
                this.selectedCSVFilePath = selectedFile.getAbsolutePath();
                this.jTextFieldCSVPath.setText(selectedCSVFilePath);

                rows = CSVUtils.GetCSVContent(selectedCSVFilePath, ";");
                int index = 0;
                for (String column : rows.get(0)) {
                    jComboBoxFolderName.addItem("Columna [" + (index + 1) + "]: " + column);
                    jComboBoxImageLinks.addItem("Columna [" + (index + 1) + "]: " + column);

                    index++;
                }

                jComboBoxFolderName.setSelectedItem(null);
                jComboBoxImageLinks.setSelectedItem(null);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        ValidateForm();
    }//GEN-LAST:event_jButton1ActionPerformed

