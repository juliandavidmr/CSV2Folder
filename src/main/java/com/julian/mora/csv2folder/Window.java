    private void jButtonProcessStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonProcessStartActionPerformed
        for (ArrayList<String> row : rows) {
            final String column = row.get(currentColumnIndexFolderName);
            final File dir = new File(selectedDestinationFolderPath, column);
            boolean resultFolderOperation = MkDir.CreateDirectory(dir.getAbsolutePath());

            if (resultFolderOperation) {
                AddTextToLog("Carpeta creada: " + dir.getAbsolutePath());
            } else {
                AddTextToLog("[WARN] Parece que la carpeta ya existe: " + dir.getAbsolutePath());
            }
        }

        JOptionPane.showMessageDialog(null, "Carpetas creadas correctamente!");

        AddTextToLog("Descargando imagenes.");

        for (ArrayList<String> row : rows) {
            final String column = row.get(currentColumnIndexFolderName);
            final String columnImages = row.get(currentColumnIndexImageLinks);
            final File dir = new File(selectedDestinationFolderPath, column);

            DriveImages.DonwloadDriveImage(columnImages, dir.getAbsolutePath());
        }
    }//GEN-LAST:event_jButtonProcessStartActionPerformed

    private void jButtonSelectDestinationFolderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSelectDestinationFolderActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        fileChooser.setDialogTitle("Seleccionar destino");
        fileChooser.setMultiSelectionEnabled(false);
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();

            selectedDestinationFolderPath = selectedFile.getAbsolutePath();
            jTextFieldDestinationFolder.setText(selectedDestinationFolderPath);
            ValidateForm();
        }
    }//GEN-LAST:event_jButtonSelectDestinationFolderActionPerformed

    private void AddTextToLog(String newLine) {
        SwingUtilities.invokeLater(() -> {
            jTextAreaLogs.append("\n" + newLine);
        });
    }

    private void ValidateForm() {
        jButtonProcessStart.setEnabled(currentColumnIndexFolderName >= 0 && currentColumnIndexImageLinks >= 0 && !selectedDestinationFolderPath.isEmpty());
    }

